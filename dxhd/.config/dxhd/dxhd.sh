#!/bin/sh

TERMINAL=st
##----------------------------------------------------------------------
## Dxhd hotkey daemon
##----------------------------------------------------------------------
##----------------------------------------------------------------------
## Toggles crontab on/off
##----------------------------------------------------------------------
# alt + g
"$HOME"/.local/bin/cron/crontog
##----------------------------------------------------------------------
## Libreofffice files
##----------------------------------------------------------------------
# super + shift + b
soffice
##----------------------------------------------------------------------
## Cmus
##----------------------------------------------------------------------
# XF86Tools
$TERMINAL -e cmus
# super + u
cmus-remote --pause
# alt + s
cmus-remote --stop
# alt + n
cmus-remote --next
# alt + p
cmus-remote --prev
##----------------------------------------------------------------------
## Newsboat and Neomutt
##----------------------------------------------------------------------
# alt + m
$TERMINAL -e neomutt
# alt + b
$TERMINAL -e newsboat
##----------------------------------------------------------------------
## Picom on and off
##----------------------------------------------------------------------
# alt + i
picom
# alt + u
pkill picom
##----------------------------------------------------------------------
##----------------------------------------------------------------------
## Function keys
##----------------------------------------------------------------------
# super + F2
"$HOME"/.dmenu/mydmenus.sh
##----------------------------------------------------------------------
# super + F3
/usr/local/bin/dmenu_run -p "Choose application" -l 10
## ----------------------------------------------------------------------
# super + F4
/usr/bin/passmenu -p "Password for:" -l 10
##----------------------------------------------------------------------
# super + F5
"$HOME"/.local/bin/dmenuunicode
##----------------------------------------------------------------------
# super + r
$TERMINAL -e /usr/bin/yazi
##----------------------------------------------------------------------
# super + F8
$TERMINAL -e /usr/bin/taskwarrior-tui
##----------------------------------------------------------------------
# super + F10
/usr/bin/tixati
##----------------------------------------------------------------------
## Volume control
##----------------------------------------------------------------------
# XF86AudioRaiseVolume
amixer set Master 5%+
# XF86AudioLowerVolume
amixer set Master 5%-
# XF86AudioMute
amixer set Master toggle
##----------------------------------------------------------------------
## Restart dwm
##----------------------------------------------------------------------
# ctrl + shift + q
pkill -RTMIN+8 dwm
##----------------------------------------------------------------------
## Kill nonresponding apps
##----------------------------------------------------------------------
# alt + period
/usr/bin/xkill
##----------------------------------------------------------------------
